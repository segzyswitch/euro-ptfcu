<script type="text/javascript">
$("#loginForm").on('submit', function(){
  event.preventDefault();

  $.ajax({
    url: "config/process.php",
    type: "POST",
    data: new FormData(this),
    cache: false,
    contentType: false,
    processData: false,
    beforeSend: function() {
    $("#loginForm .submit-btn").html("Logging in <i class='fa fa-cog fa-spin'></i>");
    },
    success: function(data) {
    $("#loginForm .submit-btn").html("Sign In");
    if ( data=='success' ) {
      window.location.href = "dashboard";
    }else {
      $("#loginForm .feedback").html(data);
    }
    },
    error: function() {
    $("#loginForm .submit-btn").html("Sign In");
    $("#loginForm .feedback").html("<div class='alert alert-danger'>An error occured, try again!</div>");
    }
  });
});
</script>
